import type { Difficulty, DraftPracticeQuestion, DraftUHRReference } from '../types/content';

type OptionText = [textSv: string, textEn: string];

function single(
  id: string,
  chapterId: string,
  ref: DraftUHRReference,
  questionSv: string,
  questionEn: string,
  options: [OptionText, OptionText, OptionText, OptionText],
  correctIndex: 0 | 1 | 2 | 3,
  explanationSv: string,
  explanationEn: string,
  tags: string[],
  difficulty: Difficulty = 'medium',
): DraftPracticeQuestion {
  const optionIds = ['a', 'b', 'c', 'd'] as const;
  return {
    id,
    chapterId,
    type: 'single_choice',
    examScope: 'uhr_based',
    questionSv,
    questionEn,
    options: options.map(([textSv, textEn], index) => ({ id: optionIds[index], textSv, textEn })),
    correctOptionId: optionIds[correctIndex],
    explanationSv,
    explanationEn,
    uhrReference: ref,
    difficulty,
    reviewStatus: 'reviewed',
    tags,
  };
}

function trueFalse(
  id: string,
  chapterId: string,
  ref: DraftUHRReference,
  questionSv: string,
  questionEn: string,
  isTrue: boolean,
  explanationSv: string,
  explanationEn: string,
  tags: string[],
  difficulty: Difficulty = 'easy',
): DraftPracticeQuestion {
  return {
    id,
    chapterId,
    type: 'true_false',
    examScope: 'uhr_based',
    questionSv,
    questionEn,
    options: [
      { id: 'true', textSv: 'Sant', textEn: 'True' },
      { id: 'false', textSv: 'Falskt', textEn: 'False' },
    ],
    correctOptionId: isTrue ? 'true' : 'false',
    explanationSv,
    explanationEn,
    uhrReference: ref,
    difficulty,
    reviewStatus: 'reviewed',
    tags,
  };
}

